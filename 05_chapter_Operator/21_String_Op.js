let s = "Hi, ";
console.log(typeof s);  // string
s += "Dev";
console.log(s); // Hi, Dev

//combining strings with + operator
console.log("Hello" + "World"); // HelloWorld

//automatically adds a space between multiple arguments.
console.log("HELLO", "testing"); // HELLO testing
console.log(1, 2, 3, 4, "Hello", true); // 1 2 3 4 Hello true